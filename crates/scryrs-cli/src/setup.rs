#[allow(clippy::too_many_arguments)]
fn setup_live<P: InitPrompt>(
    out: &mut impl Write,
    err: &mut impl Write,
    cwd: &Path,
    ingest_url: &str,
    workspace_id: &str,
    repository_id: Option<&str>,
    agent_id: &str,
    docker_network: &str,
    options: SetupExecutionOptions,
    prompt: &mut P,
) -> i32 {
    // Live setup configures consumer-project remote state; refuse it inside the
    // scryrs source checkout (`setup local` remains allowed there for dogfooding).
    if detect_scryrs_source_checkout(cwd).is_some() {
        let _ = writeln!(
            err,
            "scryrs setup: live setup is not allowed in the scryrs source repository"
        );
        let _ = writeln!(
            err,
            "Live setup must target a consumer project, not the scryrs source repository."
        );
        let _ = writeln!(
            err,
            "Run `scryrs setup live` from your target project directory instead."
        );
        return 2;
    }

    let with_compose = options.with_compose;

    // Resolve required live inputs across the shared precedence chain
    // (flags > env > .scryrs/.env > scryrs.json remote) before any writes.
    let mut resolved_inputs = remote_config::resolve_remote_inputs(
        Some(cwd),
        &RemoteOverrides {
            ingest_url: opt_nonempty(ingest_url),
            repository_id: repository_id.and_then(opt_nonempty),
            workspace_id: opt_nonempty(workspace_id),
            agent_id: opt_nonempty(agent_id),
            timeout_ms: None,
        },
    );
    let mut docker_network_value =
        remote_config::resolve_docker_network(Some(cwd), Some(docker_network));

    if let Some(missing_field) = first_missing_live_field(
        &resolved_inputs,
        docker_network_value.as_deref(),
        with_compose,
    ) {
        if options.no_interactive || !options.terminal_state.allows_prompting() {
            write_missing_live_guidance(err, missing_field, with_compose);
            return 2;
        }
        match run_live_setup_wizard(
            out,
            &mut resolved_inputs,
            &mut docker_network_value,
            with_compose,
            prompt,
        ) {
            Ok(WizardOutcome::Confirmed) => {}
            Ok(WizardOutcome::Cancelled) => return 2,
            Err(WizardError::Io(error)) => {
                let _ = writeln!(err, "scryrs setup: wizard I/O failed: {error}");
                return 1;
            }
            Err(WizardError::Prompt(InitPromptError::Cancelled)) => return 2,
            Err(WizardError::Prompt(InitPromptError::Io(error))) => {
                let _ = writeln!(err, "scryrs setup: wizard failed: {error}");
                return 1;
            }
        }
    }

    // After resolution (and any wizard collection), the required fields must be
    // present. These unwraps cannot fail given the missing-field guard above.
    let ingest_url = match resolved_inputs.ingest_url.as_deref() {
        Some(value) => value,
        None => {
            write_missing_live_guidance(err, "ingest_url", with_compose);
            return 2;
        }
    };
    let workspace_id = match resolved_inputs.workspace_id.as_deref() {
        Some(value) => value,
        None => {
            write_missing_live_guidance(err, "workspace_id", with_compose);
            return 2;
        }
    };
    let docker_network = if with_compose {
        match docker_network_value.as_deref() {
            Some(value) => Some(value),
            None => {
                write_missing_live_guidance(err, "docker_network", with_compose);
                return 2;
            }
        }
    } else {
        None
    };

    // Validate against any existing committed manifest before writing anything,
    // so a conflict leaves the filesystem untouched (no partial writes).
    let code = validate_live_state(err, cwd, ingest_url, workspace_id, docker_network);
    if code != 0 {
        return code;
    }

    // Scaffold the `.scryrs/` runtime directory (gitignore + warning-log dir).
    let code = scaffold_scryrs_live(err, cwd);
    if code != 0 {
        return code;
    }

    // Write or merge the committed `remote` section.
    let code = write_live_manifest(err, cwd, ingest_url, workspace_id, docker_network);
    if code != 0 {
        return code;
    }

    // Compose opt-in: scaffold the managed self-host stack + overrides-only .env.
    if with_compose {
        let scryrs_dir = cwd.join(".scryrs");
        let code = scaffold_env_template(err, &scryrs_dir);
        if code != 0 {
            return code;
        }
        let code = scaffold_compose_file(err, &scryrs_dir);
        if code != 0 {
            return code;
        }
    }

    if write_live_next_steps(out, ingest_url, docker_network).is_err() {
        return 1;
    }

    0
}

/// Scaffold `.scryrs/` for live mode: `.gitignore` plus the `hooks/` warning-log
/// directory. No local database is created in live mode.
fn scaffold_scryrs_live(err: &mut impl Write, base: &Path) -> i32 {
    let scryrs_dir = base.join(".scryrs");
    if let Err(e) = fs::create_dir_all(&scryrs_dir) {
        let _ = writeln!(
            err,
            "scryrs setup: cannot create {}: {e}",
            scryrs_dir.display()
        );
        return 1;
    }

    let gitignore_path = scryrs_dir.join(".gitignore");
    if !gitignore_path.exists() {
        if let Err(e) = fs::write(&gitignore_path, SCRYRS_GITIGNORE) {
            let _ = writeln!(
                err,
                "scryrs setup: cannot write {}: {e}",
                gitignore_path.display()
            );
            return 1;
        }
    }

    let hooks_dir = scryrs_dir.join("hooks");
    if let Err(e) = fs::create_dir_all(&hooks_dir) {
        let _ = writeln!(
            err,
            "scryrs setup: cannot create {}: {e}",
            hooks_dir.display()
        );
        return 1;
    }

    0
}

