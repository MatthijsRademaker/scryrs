/// Setup mode.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum SetupMode {
    /// Local SQLite trace store.
    Local,
    /// Remote ingest via scryrs server (writes project scryrs.json).
    Live,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) struct SetupExecutionOptions {
    pub(crate) no_interactive: bool,
    pub(crate) with_compose: bool,
    pub(crate) terminal_state: TerminalState,
}

impl SetupExecutionOptions {
    fn detect(no_interactive: bool, with_compose: bool) -> Self {
        Self {
            no_interactive,
            with_compose,
            terminal_state: TerminalState::detect(),
        }
    }
}

/// Execute the `scryrs setup <mode>` subcommand.
///
/// Returns the process exit code:
/// - 0: configuration scaffolded successfully
/// - 1: I/O error (cannot create directory, cannot write file)
/// - 2: usage error (unknown/missing mode, source-checkout refusal, or
///        missing/invalid/conflicting live configuration)
#[allow(clippy::too_many_arguments)]
pub fn execute_setup(
    out: &mut impl Write,
    err: &mut impl Write,
    mode: SetupMode,
    ingest_url: &str,
    workspace_id: &str,
    repository_id: Option<&str>,
    agent_id: &str,
    docker_network: &str,
    with_compose: bool,
    no_interactive: bool,
) -> i32 {
    let mut prompt = DialoguerInitPrompt::default();
    execute_setup_with_prompt(
        out,
        err,
        mode,
        ingest_url,
        workspace_id,
        repository_id,
        agent_id,
        docker_network,
        SetupExecutionOptions::detect(no_interactive, with_compose),
        &mut prompt,
    )
}

#[allow(clippy::too_many_arguments)]
pub(crate) fn execute_setup_with_prompt<P: InitPrompt>(
    out: &mut impl Write,
    err: &mut impl Write,
    mode: SetupMode,
    ingest_url: &str,
    workspace_id: &str,
    repository_id: Option<&str>,
    agent_id: &str,
    docker_network: &str,
    options: SetupExecutionOptions,
    prompt: &mut P,
) -> i32 {
    let cwd = match std::env::current_dir() {
        Ok(d) => d,
        Err(e) => {
            let _ = writeln!(err, "scryrs setup: cannot determine current directory: {e}");
            return 1;
        }
    };

    match mode {
        SetupMode::Local => setup_local(out, err, &cwd),
        SetupMode::Live => setup_live(
            out,
            err,
            &cwd,
            ingest_url,
            workspace_id,
            repository_id,
            agent_id,
            docker_network,
            options,
            prompt,
        ),
    }
}

/// Write the "mode is required" usage error (missing positional). Exit 2.
pub(crate) fn write_missing_mode(err: &mut impl Write) {
    let _ = writeln!(err, "scryrs setup: a mode (local or live) is required");
    let _ = writeln!(err, "Usage: scryrs setup <local|live>");
    let _ = writeln!(err, "See `scryrs --help`");
}

/// Write the "unknown mode" usage error, listing the supported modes. Exit 2.
pub(crate) fn write_unknown_mode(err: &mut impl Write, mode: &str) {
    let _ = writeln!(err, "scryrs setup: '{mode}' is not a supported mode");
    let _ = write!(err, "Supported modes: ");
    for (i, name) in SUPPORTED_MODES.iter().enumerate() {
        if i > 0 {
            let _ = write!(err, ", ");
        }
        let _ = write!(err, "{name}");
    }
    let _ = writeln!(err);
    let _ = writeln!(err, "See `scryrs --help`");
}

