//! Runtime trace-transport configuration (`scryrs setup <mode>`).
//!
//! `setup` owns all runtime-config scaffolding, kept deliberately separate from
//! `scryrs init` (which installs only the harness hook). Two modes:
//!
//! - **local**: scaffold the single-machine SQLite store under `.scryrs/`
//!   (`.scryrs/scryrs.db`, `.scryrs/.gitignore`). Manifest-agnostic — never
//!   touches `scryrs.json`.
//! - **live**: create-or-merge the `scryrs.json` `remote` section as the
//!   committed source of truth. Requires only `ingest_url` + `workspace_id`
//!   (`repository_id` derives from the Git remote origin and `agent_id` is
//!   autogenerated at runtime — neither is committed). The self-hosted compose
//!   stack (`.scryrs/compose.yml`, overrides-only `.scryrs/.env`, and the
//!   `docker_network`) is an explicit opt-in via `--with-compose`.

use std::fs;
use std::io::Write;
use std::path::Path;

use crate::init::detect_scryrs_source_checkout;
use crate::init_prompt::{
    DialoguerInitPrompt, InitPrompt, InitPromptError, PromptSpec, TerminalState,
};
use crate::live_bootstrap::{SCRYRS_COMPOSE_TEMPLATE, SCRYRS_DOCKER_NETWORK_ENV};
use crate::remote_config::{self, RemoteOverrides, ResolvedRemoteInputs};

/// Supported modes, listed in stable order for the usage error.
const SUPPORTED_MODES: &[&str] = &["live", "local"];

/// `.gitignore` written into `.scryrs/` so runtime trace data is never
/// committed. Ignores everything except the ignore file itself.
const SCRYRS_GITIGNORE: &str = concat!(
    "# scryrs runtime data — do not commit\n",
    "*\n",
    "!.gitignore\n",
);

/// Deterministic confirmation printed after the local `.scryrs/` store is scaffolded.
const SETUP_LOCAL_NOTE: &str = concat!(
    "Configured local trace transport: .scryrs/scryrs.db (.scryrs/.gitignore)\n",
    "\n",
    "Next steps:\n",
    "  1. Reload your agent harness for the hook to record into the local store.\n",
);

/// Overrides-only `.scryrs/.env` stub written by the compose opt-in.
///
/// The committed source of truth for live config is `scryrs.json` `remote`.
/// This gitignored file exists only so an operator can override per-machine
/// values locally; `setup` never pre-populates it with managed values.
const SCRYRS_ENV_STUB: &str = concat!(
    "# scryrs local overrides (gitignored)\n",
    "# The committed source of truth for live config is scryrs.json `remote`\n",
    "# (ingest_url, workspace_id, docker_network). repository_id is derived from\n",
    "# the Git remote origin and agent_id is autogenerated per container.\n",
    "#\n",
    "# Uncomment and set any of the following to override the committed defaults\n",
    "# on this machine only:\n",
    "#\n",
    "# SCRYRS_REMOTE_INGEST_URL=\n",
    "# SCRYRS_REPOSITORY_ID=\n",
    "# SCRYRS_WORKSPACE_ID=\n",
    "# SCRYRS_AGENT_ID=\n",
    "# SCRYRS_REMOTE_TIMEOUT_MS=\n",
    "# SCRYRS_DOCKER_NETWORK=\n",
);

