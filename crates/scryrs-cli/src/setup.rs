fn first_missing_live_field(
    resolved_inputs: &ResolvedRemoteInputs,
    docker_network: Option<&str>,
    with_compose: bool,
) -> Option<&'static str> {
    if resolved_inputs.ingest_url.is_none() {
        return Some("ingest_url");
    }
    if resolved_inputs.workspace_id.is_none() {
        return Some("workspace_id");
    }
    if with_compose && docker_network.is_none() {
        return Some("docker_network");
    }
    None
}

fn write_missing_live_guidance(err: &mut impl Write, missing_field: &str, with_compose: bool) {
    if missing_field == "docker_network" {
        let _ = writeln!(
            err,
            "scryrs setup live --with-compose: docker_network is required but not configured"
        );
        let _ = writeln!(
            err,
            "Compose scaffolding for a self-hosted live server requires a Docker network."
        );
        let _ = writeln!(
            err,
            "Provide it with --docker-network <NAME>, {SCRYRS_DOCKER_NETWORK_ENV} in the environment / .scryrs/.env, or scryrs.json remote.docker_network."
        );
        let _ = writeln!(err, "See `scryrs --help`");
        return;
    }

    let _ = writeln!(
        err,
        "scryrs setup live: {missing_field} is required but not configured"
    );
    let _ = writeln!(
        err,
        "Provide it with --{} <VALUE>, the matching SCRYRS_* environment variable / .scryrs/.env entry, or scryrs.json remote.{missing_field}.",
        missing_field.replace('_', "-")
    );
    if with_compose {
        let _ = writeln!(
            err,
            "(setup live --with-compose also requires a docker_network.)"
        );
    }
    let _ = writeln!(err, "See `scryrs --help`");
}

/// Validate the resolved live inputs against any existing committed manifest.
/// Returns 0 when safe to write, or a non-zero exit code on conflict / I/O error.
fn validate_live_state(
    err: &mut impl Write,
    cwd: &Path,
    ingest_url: &str,
    workspace_id: &str,
    docker_network: Option<&str>,
) -> i32 {
    let manifest_path = cwd.join("scryrs.json");
    let root = match load_manifest_root(err, &manifest_path) {
        Ok(root) => root,
        Err(code) => return code,
    };

    let Some(existing_remote) = root.get("remote") else {
        return 0;
    };
    let Some(existing_remote) = existing_remote.as_object() else {
        let _ = writeln!(
            err,
            "scryrs setup: existing \"remote\" in {} is not an object; refusing to overwrite",
            manifest_path.display()
        );
        return 2;
    };

    let desired = desired_remote_object(ingest_url, workspace_id, docker_network);
    for key in ["ingest_url", "workspace_id", "docker_network"] {
        let Some(current) = existing_remote.get(key).and_then(|v| v.as_str()) else {
            continue;
        };
        let desired_value = desired.get(key).and_then(|v| v.as_str());
        match desired_value {
            // Field we are writing: a divergent committed value is a conflict.
            Some(desired_value) if current != desired_value => {
                let _ = writeln!(
                    err,
                    "scryrs setup: existing remote.{key} in {} conflicts with the resolved value",
                    manifest_path.display()
                );
                let _ = writeln!(err, "See `scryrs --help`");
                return 2;
            }
            _ => {}
        }
    }

    0
}

/// Write or merge the `remote` section of the target project's `scryrs.json`.
/// Creates the file if missing; merges into an existing manifest, preserving
/// unrelated top-level keys.
fn write_live_manifest(
    err: &mut impl Write,
    cwd: &Path,
    ingest_url: &str,
    workspace_id: &str,
    docker_network: Option<&str>,
) -> i32 {
    use serde_json::Value;

    let manifest_path = cwd.join("scryrs.json");
    let mut root = match load_manifest_root(err, &manifest_path) {
        Ok(root) => root,
        Err(code) => return code,
    };

    // Merge our managed keys into any existing remote object, preserving
    // unrelated remote keys an operator may have added.
    let mut remote = root
        .get("remote")
        .and_then(Value::as_object)
        .cloned()
        .unwrap_or_default();
    for (key, value) in desired_remote_object(ingest_url, workspace_id, docker_network) {
        remote.insert(key, value);
    }

    if root.get("remote").and_then(Value::as_object) == Some(&remote) {
        return 0;
    }

    let obj = match root.as_object_mut() {
        Some(object) => object,
        None => {
            let _ = writeln!(
                err,
                "scryrs setup: internal error building scryrs.json object"
            );
            return 1;
        }
    };
    obj.insert("remote".to_string(), Value::Object(remote));

    let serialized = match serde_json::to_string_pretty(&root) {
        Ok(contents) => contents,
        Err(error) => {
            let _ = writeln!(err, "scryrs setup: cannot serialize scryrs.json: {error}");
            return 1;
        }
    };
    if let Err(error) = fs::write(&manifest_path, format!("{serialized}\n")) {
        let _ = writeln!(
            err,
            "scryrs setup: cannot write {}: {error}",
            manifest_path.display()
        );
        return 1;
    }

    0
}

/// Build the committed `remote` object. Only genuinely-shared constants are
/// committed: `ingest_url`, `workspace_id`, and (compose opt-in only)
/// `docker_network`. `repository_id` (Git-derived) and `agent_id`
/// (autogenerated per container) are intentionally absent.
fn desired_remote_object(
    ingest_url: &str,
    workspace_id: &str,
    docker_network: Option<&str>,
) -> serde_json::Map<String, serde_json::Value> {
    use serde_json::{Map, Value};

    let mut remote = Map::new();
    remote.insert(
        "ingest_url".to_string(),
        Value::String(ingest_url.to_string()),
    );
    remote.insert(
        "workspace_id".to_string(),
        Value::String(workspace_id.to_string()),
    );
    if let Some(network) = docker_network {
        remote.insert(
            "docker_network".to_string(),
            Value::String(network.to_string()),
        );
    }
    remote
}

fn load_manifest_root(
    err: &mut impl Write,
    manifest_path: &Path,
) -> Result<serde_json::Value, i32> {
    use serde_json::{Map, Value};

    if !manifest_path.exists() {
        return Ok(Value::Object(Map::new()));
    }

    let contents = match fs::read_to_string(manifest_path) {
        Ok(contents) => contents,
        Err(error) => {
            let _ = writeln!(
                err,
                "scryrs setup: cannot read {}: {error}",
                manifest_path.display()
            );
            return Err(1);
        }
    };

    match serde_json::from_str(&contents) {
        Ok(Value::Object(map)) => Ok(Value::Object(map)),
        Ok(_other) => {
            let _ = writeln!(
                err,
                "scryrs setup: {} is not a JSON object; refusing to overwrite",
                manifest_path.display()
            );
            Err(2)
        }
        Err(error) => {
            let _ = writeln!(
                err,
                "scryrs setup: cannot parse {}: {error}",
                manifest_path.display()
            );
            Err(2)
        }
    }
}

/// Scaffold `.scryrs/.env` as an overrides-only stub (compose opt-in only).
/// An existing file is preserved verbatim — it is the operator's overrides
/// surface and is never rewritten with managed values.
fn scaffold_env_template(err: &mut impl Write, scryrs_dir: &Path) -> i32 {
    let env_path = scryrs_dir.join(".env");
    if env_path.exists() {
        return 0;
    }

    if let Err(error) = fs::write(&env_path, SCRYRS_ENV_STUB) {
        let _ = writeln!(
            err,
            "scryrs setup: cannot write {}: {error}",
            env_path.display()
        );
        return 1;
    }

    0
}

fn scaffold_compose_file(err: &mut impl Write, scryrs_dir: &Path) -> i32 {
    let compose_path = scryrs_dir.join("compose.yml");
    if compose_path.exists() {
        let existing = match fs::read_to_string(&compose_path) {
            Ok(contents) => contents,
            Err(error) => {
                let _ = writeln!(
                    err,
                    "scryrs setup: cannot read {}: {error}",
                    compose_path.display()
                );
                return 1;
            }
        };
        if existing != SCRYRS_COMPOSE_TEMPLATE {
            let _ = writeln!(
                err,
                "scryrs setup: existing {} conflicts with the managed compose template",
                compose_path.display()
            );
            return 2;
        }
        return 0;
    }

    if let Err(error) = fs::write(&compose_path, SCRYRS_COMPOSE_TEMPLATE) {
        let _ = writeln!(
            err,
            "scryrs setup: cannot write {}: {error}",
            compose_path.display()
        );
        return 1;
    }

    0
}

/// Deterministic post-setup instructions for live mode.
fn write_live_next_steps(
    out: &mut impl Write,
    ingest_url: &str,
    docker_network: Option<&str>,
) -> std::io::Result<()> {
    writeln!(
        out,
        "scryrs live transport configured (scryrs.json remote is the committed source of truth: ingest_url, workspace_id)."
    )?;
    writeln!(out, "Remote server endpoint: {ingest_url}")?;
    writeln!(
        out,
        "repository_id is derived from the Git remote origin; agent_id is autogenerated per container."
    )?;
    match docker_network {
        Some(network) => {
            writeln!(out, "External Docker network: {network}")?;
            writeln!(
                out,
                "Managed self-host stack scaffolded: .scryrs/compose.yml (.scryrs/.env holds local overrides only)."
            )?;
            writeln!(out)?;
            writeln!(out, "Next steps:")?;
            writeln!(
                out,
                "  1. Ensure the `scryrs-server` image is available to Docker."
            )?;
            writeln!(out, "  2. Start the managed live server with `scryrs up`.")?;
            writeln!(
                out,
                "  3. Reload your agent harness for the hook to take effect."
            )?;
        }
        None => {
            writeln!(out)?;
            writeln!(out, "Next steps:")?;
            writeln!(
                out,
                "  1. Reload your agent harness for the hook to take effect."
            )?;
            writeln!(
                out,
                "  2. To self-host the live server, rerun with `scryrs setup live --with-compose`, then `scryrs up`."
            )?;
        }
    }
    Ok(())
}

// ---------------------------------------------------------------------------
// Interactive wizard (migrated from `init`; lives on `setup live`)
// ---------------------------------------------------------------------------

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum WizardOutcome {
    Confirmed,
    Cancelled,
}

#[derive(Debug)]
enum WizardError {
    Io(std::io::Error),
    Prompt(InitPromptError),
}

impl From<std::io::Error> for WizardError {
    fn from(error: std::io::Error) -> Self {
        Self::Io(error)
    }
}

impl From<InitPromptError> for WizardError {
    fn from(error: InitPromptError) -> Self {
        Self::Prompt(error)
    }
}

const INGEST_URL_PROMPT: PromptSpec = PromptSpec {
    field_name: "ingest_url",
    prompt: "Remote ingest URL",
};
const WORKSPACE_ID_PROMPT: PromptSpec = PromptSpec {
    field_name: "workspace_id",
    prompt: "Workspace ID",
};
const DOCKER_NETWORK_PROMPT: PromptSpec = PromptSpec {
    field_name: "docker_network",
    prompt: "Docker network name",
};

fn run_live_setup_wizard<P: InitPrompt>(
    out: &mut impl Write,
    resolved_inputs: &mut ResolvedRemoteInputs,
    docker_network: &mut Option<String>,
    with_compose: bool,
    prompt: &mut P,
) -> Result<WizardOutcome, WizardError> {
    writeln!(out, "scryrs setup live wizard")?;
    writeln!(
        out,
        "Missing required live config. Prompting only for unresolved committed fields."
    )?;
    writeln!(
        out,
        "Committed shared values will be written to scryrs.json `remote`; .scryrs/.env stays gitignored local overrides only."
    )?;
    writeln!(out)?;

    prompt_for_required_field(
        out,
        resolved_inputs,
        docker_network,
        prompt,
        INGEST_URL_PROMPT,
    )?;
    prompt_for_required_field(
        out,
        resolved_inputs,
        docker_network,
        prompt,
        WORKSPACE_ID_PROMPT,
    )?;
    if with_compose {
        prompt_for_required_field(
            out,
            resolved_inputs,
            docker_network,
            prompt,
            DOCKER_NETWORK_PROMPT,
        )?;
    }

    write_live_wizard_summary(
        out,
        resolved_inputs,
        docker_network.as_deref(),
        with_compose,
    )?;
    if !prompt.confirm("Write committed live config and continue?", true)? {
        writeln!(out, "Live setup cancelled. No files written.")?;
        return Ok(WizardOutcome::Cancelled);
    }

    Ok(WizardOutcome::Confirmed)
}

fn prompt_for_required_field<P: InitPrompt>(
    out: &mut impl Write,
    resolved_inputs: &mut ResolvedRemoteInputs,
    docker_network: &mut Option<String>,
    prompt: &mut P,
    spec: PromptSpec,
) -> Result<(), WizardError> {
    if spec.field_name == "ingest_url" && resolved_inputs.ingest_url.is_some() {
        return Ok(());
    }
    if spec.field_name == "workspace_id" && resolved_inputs.workspace_id.is_some() {
        return Ok(());
    }
    if spec.field_name == "docker_network" && docker_network.is_some() {
        return Ok(());
    }

    loop {
        writeln!(out, "{} ({})", spec.prompt, spec.field_name)?;
        let answer = prompt.prompt_text(&spec)?;
        let value = match validate_prompt_value(spec.field_name, &answer) {
            Ok(value) => value,
            Err(message) => {
                writeln!(out, "{message}")?;
                continue;
            }
        };
        set_prompt_value(resolved_inputs, docker_network, spec.field_name, value);
        return Ok(());
    }
}

fn validate_prompt_value(field_name: &str, value: &str) -> Result<String, &'static str> {
    let trimmed = value.trim();
    if trimmed.is_empty() {
        return Err("Value cannot be empty.");
    }
    if field_name == "ingest_url" && !looks_like_ingest_url(trimmed) {
        return Err("ingest_url must start with http:// or https:// and include a host.");
    }
    Ok(trimmed.to_string())
}

fn looks_like_ingest_url(value: &str) -> bool {
    ["http://", "https://"]
        .into_iter()
        .find_map(|prefix| value.strip_prefix(prefix))
        .map(|rest| !rest.is_empty() && !rest.starts_with('/'))
        .unwrap_or(false)
}

fn set_prompt_value(
    resolved_inputs: &mut ResolvedRemoteInputs,
    docker_network: &mut Option<String>,
    field_name: &str,
    value: String,
) {
    match field_name {
        "ingest_url" => resolved_inputs.ingest_url = Some(value),
        "workspace_id" => resolved_inputs.workspace_id = Some(value),
        "docker_network" => *docker_network = Some(value),
        _ => {}
    }
}

fn write_live_wizard_summary(
    out: &mut impl Write,
    resolved_inputs: &ResolvedRemoteInputs,
    docker_network: Option<&str>,
    with_compose: bool,
) -> std::io::Result<()> {
    writeln!(out)?;
    writeln!(out, "Review committed live config:")?;
    writeln!(
        out,
        "  scryrs.json remote.ingest_url = {}",
        resolved_inputs.ingest_url.as_deref().unwrap_or("<missing>")
    )?;
    writeln!(
        out,
        "  scryrs.json remote.workspace_id = {}",
        resolved_inputs
            .workspace_id
            .as_deref()
            .unwrap_or("<missing>")
    )?;
    if with_compose {
        writeln!(
            out,
            "  scryrs.json remote.docker_network = {}",
            docker_network.unwrap_or("<missing>")
        )?;
        writeln!(out, "  .scryrs/.env = local overrides only (gitignored)")?;
    }
    writeln!(out)?;
    Ok(())
}

/// Coerce a CLI flag value to `Some(trimmed)` when non-empty, else `None`.
fn opt_nonempty(value: &str) -> Option<String> {
    let trimmed = value.trim();
    if trimmed.is_empty() {
        None
    } else {
        Some(trimmed.to_string())
    }
}
